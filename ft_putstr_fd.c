#include "libft.h"
#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	if (s == NULL && *s == '\0')
		return ;
	while (*s)
	{
		ft_putchar_fd(*s, fd);
		s++;
	}
}
